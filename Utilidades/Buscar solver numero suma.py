# Buscar una combinación que sume exactamente el objetivo
def buscar_combinacion(nums, target, max_elementos=4):
    for r in range(1, max_elementos + 1):
        for combo in combinations(nums, r):
            if sum(combo) == target:
                return combo
    return None

